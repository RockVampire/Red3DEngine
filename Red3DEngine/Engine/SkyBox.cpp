SkyBox::~SkyBox()
{
    delete cubMap;
    delete skyVAO;
}

void SkyBox::loadTexture(vector<std::string> * faces)
{
    cubMap->LoadImage(faces);
}

void SkyBox::UseSkyBox(GLProgram * gragram,Camera * camera)
{
    glDepthMask(GL_FALSE);

    gragram->UseProgram();
    camera->UseCameraInSky(gragram);
    glBindTexture(GL_TEXTURE_CUBE_MAP, cubMap->TextureId);
    skyVAO->DrawVAO();

    glDepthMask(GL_TRUE);
}
